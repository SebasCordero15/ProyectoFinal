    public void cargarDatos() {
        DefaultTableModel model = (DefaultTableModel) jtServicios.getModel();
        model.setNumRows(0); // Limpiar filas existentes

        DatosServicios dtServicios = new DatosServicios();
        ArrayList<Servicios> ListaDservicios = dtServicios.todosServicios(); // Obtener datos

        String datos[] = new String[9];
        int i = 0;
        for (Agendar agend : ListaDservicios) {
            datos[0] = ListaDservicios.get(i).getNombre();
            datos[1] = ListaDservicios.get(i).getNombreP();
            datos[2] = ListaDservicios.get(i).getCedula();
            datos[3] = String.valueOf(ListaDservicios.get(i).getCantidadN());
            datos[4] = ListaDservicios.get(i).getHabitacion();
            datos[5] = String.valueOf(ListaDservicios.get(i).getFechaIngreso());
            datos[6] = String.valueOf(ListaDservicios.get(i).getFechaSalida());
            datos[7] = String.valueOf(ListaDservicios.get(i).getDogWalking());
            datos[8] = ListaDservicios.get(i).getGrooming();
            i++;
            model.addRow(datos);
        }
        jtServicios.setModel(model);

    }

